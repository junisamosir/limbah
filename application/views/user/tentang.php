				<p><span class="font-weight-bold" style="color: #568203"> Kedan Medan!</span> merupakan aplikasi berbasis web yang digunakan sebagai jasa pengangkutan limbah medis untuk mendukung kegiatan pemutusan mata rantai penularan Covid-19 khusus diseluruh wilayah kota Medan.</p>  <br>


				<h4>Mengapa harus menggunakan <span class="font-weight-bold" style="color: #568203"> Kedan Medan!</span> ?

				<hr>

				<h5 style="color: #568203" class="font-weight-bold">1. Praktis </h5>
				<p"> Penggunaan aplikasi ini dapat dilakukan <span class="font-weight-bold">#dirumahsaja</span> mengingat saat ini, segala aktivitas yang dilakukan diluar rumah perlu di minimalisasikan.</p>
				<br>

				<h5 style="color: #568203" class="font-weight-bold">2. Pengertian </h5>
				<p> Limbah medis pada penanganan Covid-19 termasuk limbah B3 (Bahan Berbahaya dan Beracun) dimana limbah tersebut sudah memiliki penanganan dalam pengolahan yang khusus dan sudah diatur sesuai prosedur. Kurangnya fasilitas pengolahan limbah B3, khususnya masyarakat biasa yang melakukan karantina mandiri yang sudah pasti menggunakan masker dan sebagainya untuk mencegah terjadinya penularan dapat membuat limbah B3 menjadi tidak terkontrol pengolahannya. Aplikasi ini memang dirancang untuk membantu rumah sakit, klinik, puskesmas, bahkan seluruh masyarakat kota Medan yang tidak memiliki fasilitas pemusnahan limbah B3.</p>
				<br>

				<h5 style="color: #568203" class="font-weight-bold"> 3. Mengurangi terjadinya kecurangan </h5>
				<p> Kurang terkontrolnya penanganan limbah medis B3 tersebut dapat menjadi peluang bagi oknum-oknum untuk melakukan pemanfaatan limbah medis B3 untuk mencari keuntungan sepihak, hal ini juga dapat menjadi salah satu jalan penularan Covid-19. </p>
				<br>

				<h5 style="color: #568203" class="font-weight-bold"> 4. Membantu Pemerintah </h5>
				<p> Aplikasi ini diharapkan dapat membantu tugas pemerintah kota Medan dalam memanajemen limbah B3 yang melunjak secara cepat akibat virus Corona.
